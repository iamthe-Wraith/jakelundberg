	<h2>Software</h2>

	<ul>
		<li>
			Most of my development is done in <a href="https://code.visualstudio.com/" target="_blank">Visual Studio Code</a> with the
			<a href="https://marketplace.visualstudio.com/items?itemName=GitHub.github-vscode-theme" target="_blank">GitHub Dark Default</a> theme.
			But I still open Vim from time to time to edit files and make quick changes. I have a custom theme called
			<a href="https://github.com/iamthe-Wraith/breeze" target="_blank">Breeze</a> I use for Vim that's a fork of the
			<a href="https://github.com/dracula/vim" target="_blank">Dracula</a> theme.
		</li>
		<li>
			A couple years ago I switched to using the <a href="https://www.warp.dev/" target="_blank">Warp</a> terminal and have never looked back.
			It's a fantastic tool with so many little benefits I could go on about it for a while...but don't worry, I won't.
		</li>
		<li>
			<a href="https://arc.net/" target="_blank">Arc</a> is my primary browser of choice, which I use for just about everything.
		</li>
		<li>
			I replaced Mac's built in Spotlight with <a href="https://www.raycast.com/" target="_blank">Raycast</a> (Pro version) for quick and easy access to
			multiple AI models and a bunch of plugins.
		</li>
		<li>
			For note taking I use <a href="https://obsidian.md/" target="_blank">Obsidian</a> with a few different vaults to help keep things organized. Particularly
			my "slip-box", which is where I keep all my notes and ideas...a method I learned from one of my favorite books,
			<a href="https://www.amazon.com/How-Take-Smart-Notes-Technique/dp/3982438802?dib=eyJ2IjoiMSJ9.YBwST8cgh8_L-Bvv5mf6aAudnMT_cy59YheFQYCu55D3WfKLzBJGqKQXcW2odHsaLpSsBydOwo9lO5UTGw416RkiRL5MvCDc6gXRRpfOoX1JgUjjnzkt3NSfRymQz-setIag6iUTzUtxYRHqZ8Cvz4uaJ5m0YVg8iD-oDzGd00C64klGpFa-NwLnNJADRSgLkPJhKZM1AVE_w1cBcXep-JRH64TlkOxsXzbcL6SJHWY.8ak35RcIBCuDIIGrgWMdHsDV-XCO2GXPv1CmkSPcXJ8&dib_tag=se&keywords=how+to+take+smart+notes&qid=1718675408&sr=8-1" target="_blank">How to Take Smart Notes</a>.
		</li>
		<li>
			<a href="https://1password.com/" target="_blank">1Password</a> is my password manager of choice.
		</li>
		<li>
			I've been a loyal <a href="https://proton.me/mail" target="_blank">Proton</a> customer for many years now. I use it for all my personal email,
			encrypted cloud storage, and VPN.
		</li>
		<li>
			I built <a href="https://buzybee.buzz" target="_blank">BuzyBee</a> to help me manage all my tasks and projects using the
			<a href="https://gettingthingsdone.com/" target="_blank">GTD Methodology</a>, and use it every day. It's not currently
			available to the public, but be sure to sign up for the waitlist if you're interested and I'll let you know as soon as it
			becomes available!
		</li>
		<li>
			I recently started using <a href="https://readwise.io/" target="_blank">Readwise</a> to manage, organize and track all my reading.
		</li>
	</ul>

